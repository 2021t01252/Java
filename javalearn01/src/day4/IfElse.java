package day4;


public class IfElse {

	public static void main(String[] args) {
		
		int age = 15;
		
		if(age>=18) {
			
			System.out.println("Eligible");
		}
		else
		{
			System.out.println("Not eligible");
		}
		
		
		
		
		
		int num = -2;
		
		if(num>0) {
			
			System.out.println("Positive");
		}
		else if(num<0) {
			
			System.out.println("Negative");
		}
		else {
			
			System.out.println("Zero");
		}
		

	}

}
